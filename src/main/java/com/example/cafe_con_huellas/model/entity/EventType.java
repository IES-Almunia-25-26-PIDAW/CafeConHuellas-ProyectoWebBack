package com.example.cafe_con_huellas.model.entity;

/**
 * Enumerado que clasifica el tipo de evento organizado por el refugio.
 */
public enum EventType {
    /** Jornada o evento orientado a facilitar adopciones. */
    ADOPCION,
    /** Mercadillo solidario para recaudar fondos. */
    MERCADILLO,
    /** Actividad educativa o de sensibilización animal. */
    EDUCACION,
    /** Evento enfocado en la recaudación de fondos o donaciones. */
    RECAUDACION,
    /** Otro tipo de evento no categorizado. */
    OTRO
}
